n = (input('Digite algo: '))
print('O tipo primitivo desse valor é ', type(n))
print('É um código ASASCII? ', n.isascii())
print('É alfabético?', n.isalnum())
print('Contém apenas letras?', n.isalpha())
print('É um número inteiro?', n.isnumeric())
print('É do tipo decimal?', n.isdecimal())
print('Tudo em letra minúscula?', n.islower())
print('Tudo em letra maiúscula?', n.isupper())


